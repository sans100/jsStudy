// 숫자와 문자, 가장 많이 사용하는 데이터 타입
// 데이터 타입
console.log(typeof '1'); // 문자 string
console.log(typeof 1); // 숫자 number

// 변수, 문자나 숫자를 담는 그릇, 변할 수 있는 값
var a = 1; // 처음 쓸 때 var를 쓴다.
a = 2; // 두번 째 부터는 생략 가능

// 비교(연산자)
console.log(1==2); // false
console.log(2==2); // true

// null : 값이 없다.
// undefined : 값이 정의되지 않았다.
// ==, === 동등 연산자
console.log(null == undefined); // true
console.log(null === undefined); // false
console.log(true == 1); // true
console.log(true === 1); // false
console.log(true == '1'); // true
console.log(true === '1'); // false
// !=, !==, <, > 부정과 부등호


